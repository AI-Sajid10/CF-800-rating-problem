#include<iostream>
using namespace std;
int main()
{
    int n,arr[4];
    cin>> n;
    int ans = 0;
    for(int j = 0; j < n; j++)
    {
        int cnt = 0;
        for(int i = 0; i < 3; i++)
        {
            cin>> arr[i];
            if(arr[i]==1)
            {
                cnt++;
            }
        }
        if(cnt >= 2)
        {
            ans++;
        }
    }
   cout<< ans;
    return 0;
}
